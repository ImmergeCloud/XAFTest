    public class Updater : ModuleUpdater
    {
        public Updater(IObjectSpace objectSpace, Version currentDBVersion) :
            base(objectSpace, currentDBVersion)
        {
        }
        public override void UpdateDatabaseAfterUpdateSchema()
        {
            base.UpdateDatabaseAfterUpdateSchema();
            string firstName = "Scott";
            string lastName = "Gross";
            Person theObject = ObjectSpace.FindObject<Person>(CriteriaOperator.Parse("FirstName=? And LastName=?", firstName, lastName));
            if (theObject == null)
            {
                theObject = ObjectSpace.CreateObject<Person>();
                theObject.FirstName = firstName;
                theObject.LastName = lastName;
            }

            ObjectSpace.CommitChanges(); //Uncomment this line to persist created object(s).
        }
        public override void UpdateDatabaseBeforeUpdateSchema()
        {
            base.UpdateDatabaseBeforeUpdateSchema();
